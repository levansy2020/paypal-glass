	{
        $paypal = new PayPal();
        $current_balance = $paypal->getBalance();

        $params = array(
            'number_of_days' => 1
        );
        $recent_history = $paypal->transactionSearch($params);

        $data = array('current_balance' => $current_balance, 'recent_history' => $recent_history);
        return View::make('index')->with('data', $data);
	}


